// RenderWord renders a word as block letters, returning each line separately.
// Each rune in the returned slices corresponds to one character of the original word.
func RenderWord(word string) [][]string {
	lines := make([][]string, LetterHeight)
	for i := range lines {
		lines[i] = make([]string, 0, len(word))
	}

	for _, char := range word {
		letter, ok := BlockLetters[char]
		if !ok {
			// Use space for unknown characters
			letter = BlockLetters[' ']
		}

		for lineIdx := 0; lineIdx < LetterHeight; lineIdx++ {
			if lineIdx < len(letter) {
				lines[lineIdx] = append(lines[lineIdx], letter[lineIdx])
			} else {
				lines[lineIdx] = append(lines[lineIdx], "")
			}
		}
	}

	return lines
}

// RenderText renders text as block letters and returns a single string with lines joined.
// This is a convenience function for simple rendering without per-character control.
func RenderText(text string) string {
	lines := RenderWord(text)
	result := make([]string, LetterHeight)

	for lineIdx := 0; lineIdx < LetterHeight; lineIdx++ {
		spacing := strings.Repeat(" ", LetterSpacing)
		result[lineIdx] = strings.Join(lines[lineIdx], spacing)
	}

	return strings.Join(result, "\n")
}

// GetLetterWidth returns the width of a letter in runes (for proper Unicode handling).
func GetLetterWidth(char rune) int {
	letter, ok := BlockLetters[char]
	if !ok || len(letter) == 0 {
		return 3
	}
	maxWidth := 0
	for _, line := range letter {
		runeCount := len([]rune(line))
		if runeCount > maxWidth {
			maxWidth = runeCount
		}
	}
	return maxWidth
}

// GetTotalWidth returns the total display width of a word in the block font.
func GetTotalWidth(word string) int {
	total := 0
	runes := []rune(word)
	for i, char := range runes {
		total += GetLetterWidth(char)
		if i < len(runes)-1 {
			total += LetterSpacing
		}
	}
	return total
}
